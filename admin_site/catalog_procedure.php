            <h1>Catalog Procedure</h1>
              <h5><u>You will need:</u></h5>
              <ul style="list">
                <li>A provenience of artifacts to catalog (ex. TU 1, Level B, Bag Log number 8OR471.2)</li>
                <li>Cataloging kit (4 mil bags, catalog tags, sharpies, catalog sheets, pens)</li>
                <li>Trays</li>
                <li>Scale</li>
                <li>½ inch geological screen (for separating crumb sherds)</li>
                <li>The appropriate catalog binder for the site you are working on</li>
                <li>The Cataloging Procedure Manual</li>
              </ul>
              Begin by sorting the bag of artifacts by <b>Material</b>
              <ul>
                <li>Pottery, Invertebrate, Lithic, etc.</li>
              </ul>
              Once sorted by material, then sort by <b>Material Type</b>. For assistance follow the material type section of the procedure manual
              <ul>
                <li>Limestone tempered, Sand tempered, Crown Conch, Lightning Whelk, etc</li>
              </ul>
              Then sort each material type by <b>Form</b>, also according to the categories in the procedure manual
              <ul>
                <li>Body, Rim, Crumb, Hammer, Columella, Bead, etc.</li>
                <li><b>Pottery:</b> Place all non-rim sherds in the <u>½ inch geological</u> screen to separate the crumb sherds. All sherds that fall through are crumb sherds; they will be cataloged together by temper (if identifiable). All rim sherds will be cataloged as such, regardless of size.</li>
              </ul>
              All <u>pottery</u> will also have <b>Surface Treatment</b> and <b>Decoration</b> qualifiers. These are very specific and Decoration is dependent upon the type of Surface Treatment. Please follow the procedure guidelines carefully.
              <ul>
                <li><b>Surface Treatment:</b> Stamped, Incised, Plain, etc.</li>
                <li><b>Decoration:</b> Cord Marked, Linear Check Stamped, Brushed, Burnished, Linear, etc.</li>
              </ul>
              <b>Modification</b> of any material should be recorded as well and cataloged according to modification. There are different modifications for particular material types, the manual will help.
              <ul>
                <li>Ex. Perforated 1 hole, Perforated 2 holes, Mend hole, Retouched, Residue, Sooted</li>
              </ul>
              <h5><u>Write Catalog Bags</u></h5>
              <ul>
                <li>All cataloged items will be placed in 4 mil bags.</li>
                <li>Info written on the outside of the 4 mil bag includes Full Catalog Number (ex. 8LV68.2.1), Test Unit, Level, and Material, Material Type, and/or Form (Chert, Flake)</li>
                <li>Write clearly on all bags and tags, ensuring to record provenience information accurately.</li>
                <li>Each bag has to have a catalog tag filled out and placed inside, facing the back of the bag.</li>
                <li>Use the bag size that is most appropriate for the items being placed inside, do not jam large items in small bags or use large bags for smaller items. Alert your supervisor if bags or tags are running low.</li>
                <li>Pack all cataloged items in an overall bag. You may need to have multiple bags (label with 1 of 2, 2 of 2, etc).</li>
              </ul>
              <h5><u>Things to Keep in Mind:</u></h5>
              <ul>
                <li>Ask your professor if you have any questions during analysis, better to get it right the first time, there are no silly questions.</li>
                <li>Never leave a tray unlabeled, there are post-it notes and scrap paper if you need them. A bag log number will suffice.</li>
                <li>Use the microscope whenever you need to, especially when deciphering temper types for pottery.</li>
                <li>Leave time at the end of your session for cleanup, wiping down tables, putting away all materials, sweeping the floor, and labeling all of your trays clearly.</li>
              </ul>




